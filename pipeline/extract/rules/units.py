def _round(x: float) -> float:
    r = round(x, 10)
    # избавляемся от -0.0 и представляем целые чисто
    if r == int(r):
        return float(int(r))
    return r


def canonicalize_unit(value: float, unit: Optional[str]) -> Tuple[Optional[float], Optional[str]]:
    """Канонизирует ``value`` в единице ``unit``.

    Возвращает кортеж ``(value_canonical, unit_canonical)``.
    Неизвестная единица возвращается без изменений.
    ``unit is None`` -> ``(value, None)``.
    """
    if unit is None:
        return value, None
    key = normalize_unit(unit)

    # 1) физические СИ-величины через pint
    if _HAVE_PINT and key in _PINT_MAP:
        src, tgt, label = _PINT_MAP[key]
        try:
            if key in _TEMP_KEYS:
                q = _UREG.Quantity(value, src).to(tgt)
            else:
                q = (value * _UREG(src)).to(tgt)
            return _round(float(q.magnitude)), label
        except Exception:
            pass  # fallback ниже

    # 2) доменная линейная таблица
    if key in _LINEAR:
        factor, offset, label = _LINEAR[key]
        return _round(value * factor + offset), label

    # 3) fallback без pint для физических единиц (линейные множители)
    _PHYS_FALLBACK = {
        "МПа": (1.0, 0.0, "МПа"), "кПа": (1e-3, 0.0, "МПа"),
        "ГПа": (1e3, 0.0, "МПа"), "гПа": (1e-4, 0.0, "МПа"),
        "Па": (1e-6, 0.0, "МПа"), "атм": (0.101325, 0.0, "МПа"),
        "бар": (0.1, 0.0, "МПа"), "К": (1.0, -273.15, "°C"), "K": (1.0, -273.15, "°C"),
        "мкм": (1e-3, 0.0, "мм"), "нм": (1e-6, 0.0, "мм"), "см": (10.0, 0.0, "мм"),
        "мм": (1.0, 0.0, "мм"), "м3/ч": (1.0, 0.0, "м³/ч"), "л/ч": (1e-3, 0.0, "м³/ч"),
        "л/с": (3.6, 0.0, "м³/ч"), "м/с": (1.0, 0.0, "м/с"),
    }
    if key in _PHYS_FALLBACK:
        factor, offset, label = _PHYS_FALLBACK[key]
        return _round(value * factor + offset), label

    # 4) неизвестно — не искажаем
    return value, unit
