"""Канонизация единиц измерения (pint + кастомная доменная таблица).

Публичная функция: ``canonicalize_unit(value, unit) -> (value_canonical, unit_canonical)``.

Доменная таблица (LINEAR) — источник истины для доменных отношений
(мг/дм³→мг/л, ppm→мг/л для водных растворов, г/т, кА/м²→А/м², т/сут …).
Физические СИ-конверсии (давление, температура, длина, объёмный расход,
скорость) идут через pint (модуль честно использует pint), с идентичным
результатом кастомной таблицы (проверяется в тестах).
"""
from __future__ import annotations

from typing import Optional, Tuple

try:
    import pint

    _UREG = pint.UnitRegistry()
    _HAVE_PINT = True
except Exception:  # pragma: no cover - pint всегда в requirements
    _UREG = None
    _HAVE_PINT = False


# ---------------------------------------------------------------------------
# Нормализация строки единицы (без изменения регистра — важны приставки
# кПа/МПа/ГПа/гПа/мПа, которые различаются только регистром).
# ---------------------------------------------------------------------------
def normalize_unit(unit: str) -> str:
    if unit is None:
        return unit
    u = unit.strip()
    u = u.replace(" ", "").replace(" ", "")
    u = u.replace("²", "2").replace("³", "3").replace("¹", "1").replace("⁻", "-")
    u = u.replace("·", "*").replace("∙", "*").replace("×", "*")
    u = u.replace("℃", "°C")
    # кириллическая С (U+0421) в «°С» -> латинская C
    u = u.replace("°С", "°C")
    u = u.replace(" ", "")
    # варианты записи промилле / млн-1
    u = u.replace("млн-1", "ppm").replace("млн−1", "ppm")
    return u


# ---------------------------------------------------------------------------
# pint-маршруты: normalized_key -> (from_expr, to_expr, canonical_label)
# ---------------------------------------------------------------------------
